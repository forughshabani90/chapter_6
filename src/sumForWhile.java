import java.util.Scanner;

public class sumForWhile {
    public static void main(String[] args) {
        for (int i = 1; i < 10; i++) {
            System.out.println(i);
        }
        for (int k = 1; k < 10; k++) {
            for (int j = 1; j < 10; j++) {
                System.out.println(k * j);

            }
        }

    }
}

